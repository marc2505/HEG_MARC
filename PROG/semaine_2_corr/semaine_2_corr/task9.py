""" calcul cercle
données: rayon
résultat: affiche la surface et le périmètre d'un cercle
"""

### déclaration et initilisation de variables

surface: float
perimetre: float
rayon : float = 18
pi : float = 3.1415927



### séquence d'opérations
### à coder ###

surface = pi*rayon**2
perimetre = 2*pi*rayon




### fin code ###

print("surface:", surface)
print("périmètre:", perimetre)
